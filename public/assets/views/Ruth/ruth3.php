                <p><strong>Ruth and Boaz at the Threshing Floor</strong></p>
                <p>One day Ruth’s mother-in-law Naomi said to her, “My daughter, I must find a home for you, where you will be well provided for. <strong>2</strong> Now Boaz, with whose women you have worked, is a relative of ours. Tonight he will be winnowing barley on the threshing floor. <strong>3</strong> Wash, put on perfume, and get dressed in your best clothes. Then go down to the threshing floor, but don’t let him know you are there until he has finished eating and drinking. <strong>4</strong> When he lies down, note the place where he is lying. Then go and uncover his feet and lie down. He will tell you what to do.”</p>
                <p><strong>5</strong> “I will do whatever you say,” Ruth answered. <strong>6</strong> So she went down to the threshing floor and did everything her mother-in-law told her to do.</p>
                <p><strong>7</strong> When Boaz had finished eating and drinking and was in good spirits, he went over to lie down at the far end of the grain pile. Ruth approached quietly, uncovered his feet and lay down. <strong>8</strong> In the middle of the night something startled the man; he turned—and there was a woman lying at his feet!</p>
                <p><strong>9</strong> “Who are you?” he asked.</p>
                <p>“I am your servant Ruth,” she said. “Spread the corner of your garment over me, since you are a guardian-redeemer of our family.”</p>
                <p><strong>10</strong> “The Lord bless you, my daughter,” he replied. “This kindness is greater than that which you showed earlier: You have not run after the younger men, whether rich or poor. <strong>11</strong> And now, my daughter, don’t be afraid. I will do for you all you ask. All the people of my town know that you are a woman of noble character. <strong>12</strong> Although it is true that I am a guardian-redeemer of our family, there is another who is more closely related than I. <strong>13</strong> Stay here for the night, and in the morning if he wants to do his duty as your guardian-redeemer, good; let him redeem you. But if he is not willing, as surely as the Lord lives I will do it. Lie here until morning.”</p>
                <p><strong>14</strong> So she lay at his feet until morning, but got up before anyone could be recognized; and he said, “No one must know that a woman came to the threshing floor.”</p>
                <p><strong>15</strong> He also said, “Bring me the shawl you are wearing and hold it out.” When she did so, he poured into it six measures of barley and placed the bundle on her. Then he went back to town.</p>
                <p><strong>16</strong> When Ruth came to her mother-in-law, Naomi asked, “How did it go, my daughter?”</p>
                <p>Then she told her everything Boaz had done for her <strong>17</strong> and added, “He gave me these six measures of barley, saying, ‘Don’t go back to your mother-in-law empty-handed.’”</p>
                <p><strong>18</strong> Then Naomi said, “Wait, my daughter, until you find out what happens. For the man will not rest until the matter is settled today.”</p>
