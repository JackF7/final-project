                <p><strong>Boaz Marries Ruth</strong></p>
                <p>Meanwhile Boaz went up to the town gate and sat down there just as the guardian-redeemer he had mentioned came along. Boaz said, “Come over here, my friend, and sit down.” So he went over and sat down.</p>
                <p><strong>2</strong> Boaz took ten of the elders of the town and said, “Sit here,” and they did so. <strong>3</strong> Then he said to the guardian-redeemer, “Naomi, who has come back from Moab, is selling the piece of land that belonged to our relative Elimelek. <strong>4</strong> I thought I should bring the matter to your attention and suggest that you buy it in the presence of these seated here and in the presence of the elders of my people. If you will redeem it, do so. But if you will not, tell me, so I will know. For no one has the right to do it except you, and I am next in line.”</p>
                <p>“I will redeem it,” he said.</p>
                <p><strong>5</strong> Then Boaz said, “On the day you buy the land from Naomi, you also acquire Ruth the Moabite, the dead man’s widow, in order to maintain the name of the dead with his property.”</p>
                <p><strong>6</strong> At this, the guardian-redeemer said, “Then I cannot redeem it because I might endanger my own estate. You redeem it yourself. I cannot do it.”</p>
                <p><strong>7</strong> (Now in earlier times in Israel, for the redemption and transfer of property to become final, one party took off his sandal and gave it to the other. This was the method of legalizing transactions in Israel.)</p>
                <p><strong>8</strong> So the guardian-redeemer said to Boaz, “Buy it yourself.” And he removed his sandal.</p>
                <p><strong>9</strong> Then Boaz announced to the elders and all the people, “Today you are witnesses that I have bought from Naomi all the property of Elimelek, Kilion and Mahlon. <strong>10</strong> I have also acquired Ruth the Moabite, Mahlon’s widow, as my wife, in order to maintain the name of the dead with his property, so that his name will not disappear from among his family or from his hometown. Today you are witnesses!”</p>
                <p><strong>11</strong> Then the elders and all the people at the gate said, “We are witnesses. May the Lord make the woman who is coming into your home like Rachel and Leah, who together built up the family of Israel. May you have standing in Ephrathah and be famous in Bethlehem. <strong>12</strong> Through the offspring the Lord gives you by this young woman, may your family be like that of Perez, whom Tamar bore to Judah.”</p>
                <p><strong>Naomi Gains a Son</strong></p>
                <p><strong>13</strong> So Boaz took Ruth and she became his wife. When he made love to her, the Lord enabled her to conceive, and she gave birth to a son. <strong>14</strong> The women said to Naomi: “Praise be to the Lord, who this day has not left you without a guardian-redeemer. May he become famous throughout Israel! <strong>15</strong> He will renew your life and sustain you in your old age. For your daughter-in-law, who loves you and who is better to you than seven sons, has given him birth.”</p>
                <p><strong>16</strong> Then Naomi took the child in her arms and cared for him. <strong>17</strong> The women living there said, “Naomi has a son!” And they named him Obed. He was the father of Jesse, the father of David.</p>
                <p><strong>The Genealogy of David</strong></p>
                <p><strong>18</strong> This, then, is the family line of Perez:</p>
                    <ul>
                        <p>Perez was the father of Hezron,</p>
                        <p><strong>19</strong> Hezron the father of Ram,</p>
                        <p>Ram the father of Amminadab,</p>
                        <p><strong>20</strong> Amminadab the father of Nahshon,</p>
                        <p>Nahshon the father of Salmon,</p>
                        <p><strong>21</strong> Salmon the father of Boaz,</p>
                        <p>Boaz the father of Obed,</p>
                        <p><strong>22</strong> Obed the father of Jesse,</p>
                        <p>and Jesse the father of David.</p>
